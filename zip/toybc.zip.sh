PROJ="toybc"

################################################################################

cd "${HOME}/Projects/aiurovet.github.io/" &&\
mkdir -p zip &&\
rm -f "zip/${PROJ}.zip" &&\
zip -r -9 "zip/${PROJ}.zip" "${PROJ}"/* &&\
exit 0

exit 1

