$(document).ready(function () {
  initHeader();
});

////////////////////////////////////////////////////////////////////////////////

function initHeader() {
  const jqHeader = $('.header');

  if (!jqHeader.hasClass('home')) {
    jqHeader.click(onHeaderClick);
  }
}

////////////////////////////////////////////////////////////////////////////////

function onHeaderClick() {
  document.location = 'index.html';
}

