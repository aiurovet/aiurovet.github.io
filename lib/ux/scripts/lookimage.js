"use strict";

////////////////////////////////////////////////////////////////////////////////

class LookImage {
  // Constants
  //
  static defaultAlignment = new Alignment(Alignment.center + Alignment.middle + Alignment.stretchX + Alignment.stretchY);

  //////////////////////////////////////////////////////////////////////////////

  // Background image alignment (not implemented)
  //
  alignment = null;

  // Background image URL (not implemented)
  //
  url = null;

  //////////////////////////////////////////////////////////////////////////////
  // If only one argument passed, and that is an object, initialize parameters
  // with its properties: i.e. constructing from a deserialized saved object
  //////////////////////////////////////////////////////////////////////////////

  constructor(url, alignment) {
    if ((url instanceof Object) && ("url" in url)) {
      let from = url;
      this.init(from.url, from.alignment)
    } else {
      this.init(url, alignment);
    }
  }

  //////////////////////////////////////////////////////////////////////////////
  // Initialise properties from the given parameters
  //////////////////////////////////////////////////////////////////////////////

  init(url, alignment) {
    this.url = url ? url : null;
    this.alignment = alignment ? new Alignment(alignment) : LookImage.defaultAlignment;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Convert this object into the one that excludes all irrelevant properties
  //////////////////////////////////////////////////////////////////////////////

  toSerializable() {
    return {
      url: this.url,
      alignment: this.alignment.toSerializable(),
    };
  }

  //////////////////////////////////////////////////////////////////////////////
}