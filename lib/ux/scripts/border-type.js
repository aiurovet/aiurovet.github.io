"use strict";

////////////////////////////////////////////////////////////////////////////////

class BorderType {
  // Constants
  //
  static bottom = "B";
  static left = "L";
  static none = "-";
  static right = "R";
  static top = "T";

  static bottomLeft = BorderType.bottom + BorderType.left;
  static bottomRight = BorderType.bottom + BorderType.right;
  static topLeft = BorderType.top + BorderType.left;
  static topRight = BorderType.top + BorderType.right;

  static full = BorderType.topLeft + BorderType.bottomRight;

  static defaultValue = BorderType.full;

  //////////////////////////////////////////////////////////////////////////////

  // Current alignment
  //
  value = null;

  //////////////////////////////////////////////////////////////////////////////
  // If only one argument passed, and that is an object, initialize parameters
  // with its properties: i.e. constructing from a deserialized saved object
  //////////////////////////////////////////////////////////////////////////////

  constructor(value) {
    if ((value instanceof Object) && ("value" in value)) {
      let from = value;
      this.init(from.value)
    } else {
      this.init(value);
    }
  }

  //////////////////////////////////////////////////////////////////////////////
  // Check two objects have the same value
  // or this value is the same as the one passed as the argument
  //////////////////////////////////////////////////////////////////////////////

  equals(v2) {
    return this.value.consistsOf(v2);
  }

  //////////////////////////////////////////////////////////////////////////////
  // Initialise properties from the given parameters
  //////////////////////////////////////////////////////////////////////////////

  init(value) {
    this.value = value ?? Alignment.defaultValue;
  }

  //////////////////////////////////////////////////////////////////////////////
  // Convert this object into the one that excludes all irrelevant properties
  //////////////////////////////////////////////////////////////////////////////

  toSerializable() {
    return this.value;
  }

  //////////////////////////////////////////////////////////////////////////////
}