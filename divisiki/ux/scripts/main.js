"use strict";

////////////////////////////////////////////////////////////////////////////////
// Global variables (singletons)
////////////////////////////////////////////////////////////////////////////////

var Core = null;
var Data = null;
var Json = null;
var Main = null;
var Pref = null;

////////////////////////////////////////////////////////////////////////////////
// Application entry point
////////////////////////////////////////////////////////////////////////////////

$(document).ready(() => {
  // Initialize singletons
  //
  Json = new JsonClass();
  Data = (new DataClass()).load();
  Core = new CoreClass();
  Pref = new PrefClass();
  Main = new MainClass();

  // Initialize the UI
  //
  Main.setNumber();
  Main.onClickPlay(false);
  Main.setDivisor(2);
  Main.setScore(0);
  //Pref.init(true);
});


////////////////////////////////////////////////////////////////////////////////
// Set the event handlers
////////////////////////////////////////////////////////////////////////////////

$(window).bind("beforeunload", async () => {
  if (!Pref) {
    return;
  }
  await Pref.onClickExit();
});

$(window).resize(() => {
  if (!Core) {
    return;
  }
  Main.setNumberHeight();
});

////////////////////////////////////////////////////////////////////////////////

class MainClass {
  constructor() {
  }

  //////////////////////////////////////////////////////////////////////////////
  // Go to the menu
  //////////////////////////////////////////////////////////////////////////////

  onClickMenu() {
    Core.setPopup(true);
    Pref.onClickCanEdit();
    Pref.onClickHelp(false);
    Pref.setEditorHeight();
    Pref.resetList();
  }

  //////////////////////////////////////////////////////////////////////////////
  // Go to the menu
  //////////////////////////////////////////////////////////////////////////////

  onClickPlay(isPlay) {
    var visibleStyle = "flex";

    if (isPlay) {
      Core.setVisible($("#play"), false);
      Core.setVisible($("#number"), true, visibleStyle);
    } else {
      Core.setVisible($("#number"), false);
      Core.setVisible($("#play"), true, visibleStyle);
    }

    Main.setNumberHeight();
    $("#action-no, #action-yes").css("opacity", (isPlay ? 1.0 : 0.25));
  }

  //////////////////////////////////////////////////////////////////////////////
  // Reaction: a number is NOT divisible by given number(s)
  //////////////////////////////////////////////////////////////////////////////

  onClickNo() {
  }

  //////////////////////////////////////////////////////////////////////////////
  // Click handler to select a user
  //////////////////////////////////////////////////////////////////////////////

  onClickSetUser() {
    Core.setVisible($("#popup-user"), true, "flex");
    Core.setPopup(true);
  }

  //////////////////////////////////////////////////////////////////////////////
  // Reaction: a number is divisible by given number(s)
  //////////////////////////////////////////////////////////////////////////////

  onClickYes() {
  }

  //////////////////////////////////////////////////////////////////////////////

  setDivisor(value) {
    $("#divisor").text(value);
    Data.getSelectedGame().divisors.splice(0, 0, value);
    Data.save();
  }

  //////////////////////////////////////////////////////////////////////////////

  setNumber() {
    $("#number").text(Data.getSelectedGame().getNextNumber());
    this.setNumberHeight();
  }

  //////////////////////////////////////////////////////////////////////////////

  setNumberHeight() {
    var jqNumber = $("#number"); 
    var maxHeight = $("#action-no").innerHeight();
    var maxWidth = jqNumber.innerWidth();
    var numLen = (jqNumber.text().length || 1);
    var curWidth = ((maxWidth / numLen) * (15. / 12));

    if (curWidth > maxWidth) {
      curWidth = maxWidth;
    }

    if (curWidth > maxHeight) {
      curWidth = maxHeight;
    }

    jqNumber.css("font-size", `${curWidth}px`);
  }

  //////////////////////////////////////////////////////////////////////////////

  setScore(value) {
    $("#user").text(Data.getSelectedUser().userId);
    $("#score").text(value);

    if (Data.getSelectedGame().setMaxScore(value)) {
      Data.save();
    }
  }

  //////////////////////////////////////////////////////////////////////////////
}